#include <stdlib.h>
#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*ptr;
	size_t	i;

	i = 0;
	if (ft_strlen(s) <= start)
	{
		ptr = malloc(1);
		*ptr = '\0';
		return (ptr);
	}
	ptr = (char *)malloc(len + 1);
	if (!ptr || !s)
		return (NULL);
	while (s[start + i] && i < len)
	{
		ptr[i] = s[start + i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
